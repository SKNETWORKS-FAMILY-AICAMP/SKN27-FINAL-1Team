-- Bobbeori MVP PostgreSQL schema
-- 밥벌이 MVP PostgreSQL 스키마

CREATE TABLE users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR(255) NOT NULL UNIQUE,
    nickname VARCHAR(100) NOT NULL,
    auth_provider VARCHAR(50),
    provider_user_id VARCHAR(255),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE users IS '사용자';
COMMENT ON COLUMN users.id IS '사용자ID';
COMMENT ON COLUMN users.email IS '이메일';
COMMENT ON COLUMN users.nickname IS '닉네임';
COMMENT ON COLUMN users.auth_provider IS '가입채널';
COMMENT ON COLUMN users.provider_user_id IS '소셜고유ID';
COMMENT ON COLUMN users.created_at IS '생성일시';

CREATE TABLE user_preferences (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL UNIQUE REFERENCES users(id) ON DELETE CASCADE,
    allergies TEXT,
    disliked_ingredients TEXT,
    preferred_ingredients TEXT,
    allow_expiry_alert BOOLEAN NOT NULL DEFAULT TRUE
);

COMMENT ON TABLE user_preferences IS '사용자설정';
COMMENT ON COLUMN user_preferences.id IS '사용자설정ID';
COMMENT ON COLUMN user_preferences.user_id IS '사용자ID';
COMMENT ON COLUMN user_preferences.allergies IS '알레르기정보';
COMMENT ON COLUMN user_preferences.disliked_ingredients IS '비선호식재료';
COMMENT ON COLUMN user_preferences.preferred_ingredients IS '선호식재료';
COMMENT ON COLUMN user_preferences.allow_expiry_alert IS '소비기한알림허용여부';

CREATE TABLE calendar_integrations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    provider VARCHAR(50) NOT NULL DEFAULT 'google',
    access_token TEXT NOT NULL,
    refresh_token TEXT,
    expires_at TIMESTAMPTZ,
    calendar_id VARCHAR(255) NOT NULL DEFAULT 'primary',
    connected_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_calendar_integrations_user_provider UNIQUE (user_id, provider)
);

CREATE INDEX idx_calendar_integrations_user_id ON calendar_integrations(user_id);

COMMENT ON TABLE calendar_integrations IS '캘린더연동';
COMMENT ON COLUMN calendar_integrations.id IS '캘린더연동ID';
COMMENT ON COLUMN calendar_integrations.user_id IS '사용자ID';
COMMENT ON COLUMN calendar_integrations.provider IS '캘린더제공자';
COMMENT ON COLUMN calendar_integrations.access_token IS '암호화AccessToken';
COMMENT ON COLUMN calendar_integrations.refresh_token IS '암호화RefreshToken';
COMMENT ON COLUMN calendar_integrations.expires_at IS 'AccessToken만료일시';
COMMENT ON COLUMN calendar_integrations.calendar_id IS '캘린더ID';
COMMENT ON COLUMN calendar_integrations.connected_at IS '연결일시';
COMMENT ON COLUMN calendar_integrations.updated_at IS '수정일시';

CREATE TABLE calendar_event_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    event_key VARCHAR(255) NOT NULL,
    event_type VARCHAR(50),
    summary VARCHAR(255),
    target_date DATE,
    google_event_id VARCHAR(255),
    html_link VARCHAR(500),
    status VARCHAR(30) NOT NULL,
    source VARCHAR(30) NOT NULL DEFAULT 'manual',
    error_message TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_calendar_event_logs_user_id ON calendar_event_logs(user_id);
CREATE INDEX idx_calendar_event_logs_created_at ON calendar_event_logs(created_at);
CREATE INDEX idx_calendar_event_logs_event_key ON calendar_event_logs(event_key);

CREATE TABLE ingredients (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    normalized_name VARCHAR(100) NOT NULL UNIQUE,
    category VARCHAR(100),
    default_unit VARCHAR(30),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE ingredients IS '식재료';
COMMENT ON COLUMN ingredients.id IS '식재료ID';
COMMENT ON COLUMN ingredients.name IS '식재료명';
COMMENT ON COLUMN ingredients.normalized_name IS '정규화명';
COMMENT ON COLUMN ingredients.category IS '분류';
COMMENT ON COLUMN ingredients.default_unit IS '기본단위';
COMMENT ON COLUMN ingredients.created_at IS '생성일시';

CREATE TABLE ingredient_aliases (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ingredient_id BIGINT NOT NULL REFERENCES ingredients(id) ON DELETE CASCADE,
    alias_name VARCHAR(100) NOT NULL,
