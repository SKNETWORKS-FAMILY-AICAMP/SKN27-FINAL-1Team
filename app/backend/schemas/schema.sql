COMMENT ON TABLE ingredient_aliases IS '식재료별칭';
COMMENT ON COLUMN ingredient_aliases.id IS '별칭ID';
COMMENT ON COLUMN ingredient_aliases.ingredient_id IS '식재료ID';
COMMENT ON COLUMN ingredient_aliases.alias_name IS '별칭명';
COMMENT ON COLUMN ingredient_aliases.created_at IS '생성일시';

CREATE TABLE ingredient_storage_standards (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
