COMMENT ON COLUMN receipt_items.raw_item_name IS '원본품목명';
COMMENT ON COLUMN receipt_items.normalized_item_name IS '정규화품목명';
COMMENT ON COLUMN receipt_items.quantity IS '수량';
COMMENT ON COLUMN receipt_items.unit IS '단위';
COMMENT ON COLUMN receipt_items.price IS '가격';
COMMENT ON COLUMN receipt_items.is_food IS '식품여부';
COMMENT ON COLUMN receipt_items.memo IS '메모';
COMMENT ON COLUMN receipt_items.created_at IS '생성일시';

CREATE TABLE fridge_items (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    ingredient_id BIGINT NOT NULL REFERENCES ingredients(id) ON DELETE RESTRICT,
    receipt_item_id BIGINT REFERENCES receipt_items(id) ON DELETE SET NULL,
    display_name VARCHAR(255),
    quantity NUMERIC(10, 2),
    unit VARCHAR(30),
    storage_location VARCHAR(50),
    purchased_date DATE,
    expiry_date DATE,
    status VARCHAR(30) NOT NULL DEFAULT 'normal',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ck_fridge_items_status
        CHECK (status IN ('normal', 'expiring', 'expired', 'used'))
);

