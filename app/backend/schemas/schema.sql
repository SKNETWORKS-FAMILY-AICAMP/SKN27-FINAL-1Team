CREATE TABLE notifications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    fridge_item_id BIGINT NOT NULL REFERENCES fridge_items(id) ON DELETE CASCADE,
    notification_type VARCHAR(50) NOT NULL,
    target_date DATE,
    message TEXT,
    status VARCHAR(30) NOT NULL DEFAULT 'pending',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    sent_at TIMESTAMPTZ,
    CONSTRAINT ck_notifications_type
        CHECK (notification_type IN ('expiring_soon', 'expired')),
    CONSTRAINT ck_notifications_status
        CHECK (status IN ('pending', 'sent', 'read', 'failed'))
);

COMMENT ON TABLE notifications IS '알림';
COMMENT ON COLUMN notifications.id IS '알림ID';
COMMENT ON COLUMN notifications.user_id IS '사용자ID';
COMMENT ON COLUMN notifications.fridge_item_id IS '보유재료ID';
COMMENT ON COLUMN notifications.notification_type IS '알림유형';
COMMENT ON COLUMN notifications.target_date IS '대상일자';
COMMENT ON COLUMN notifications.message IS '메시지';
COMMENT ON COLUMN notifications.status IS '상태';
COMMENT ON COLUMN notifications.created_at IS '생성일시';
COMMENT ON COLUMN notifications.sent_at IS '발송일시';

