COMMENT ON COLUMN ingredient_storage_standards.storage_location IS '보관위치';
COMMENT ON COLUMN ingredient_storage_standards.lifespan_days IS '보관가능일수';
COMMENT ON COLUMN ingredient_storage_standards.created_at IS '생성일시';

CREATE TABLE receipts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    original_file_name VARCHAR(255),
    store_name TEXT,
    purchased_at TIMESTAMPTZ,
    total_price INTEGER,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
